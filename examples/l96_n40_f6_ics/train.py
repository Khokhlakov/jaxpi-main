def _load_rollout_pool(max_additions: int, num_initial_ics: int,
                       num_vars: int, mat_path="data/train_rollouts.mat") -> jnp.ndarray:
    """
    Load pre-computed rollout ICs from a .mat file generated by
    generate_train_rollouts.py.
 
    Expected keys in the .mat file:
        u0_original      : (num_initial_ics, N)
        u0_rollout_1     : (num_initial_ics, N)
        ...
        u0_rollout_K     : (num_initial_ics, N)   K = max_additions
 
    Returns:
        u0_pool : (num_initial_ics * (max_additions + 1), N) — full pre-built
                  pool with original ICs in slot 0 and rollout ICs in slots 1…K.
    """
    data = loadmat(mat_path)
 
    total_pool_size = num_initial_ics * (max_additions + 1)
    u0_pool = np.zeros((total_pool_size, num_vars), dtype=np.float32)
 
    u0_pool[:num_initial_ics] = data["u0_original"].astype(np.float32)
 
    for addition in range(1, max_additions + 1):
        key = f"u0_rollout_{addition}"
        if key not in data:
            raise KeyError(
                f"Key '{key}' missing from {mat_path}. "
                f"Regenerate with max_additions >= {addition}."
            )
        slot_start = num_initial_ics * addition
        slot_end   = slot_start + num_initial_ics
        u0_pool[slot_start:slot_end] = data[key].astype(np.float32)
 
    logging.info(
        f"Loaded rollout pool from {mat_path}: "
        f"{max_additions + 1} slots × {num_initial_ics} ICs = {total_pool_size} rows."
    )
    return jnp.array(u0_pool)
 
 
def _expand_pool_model(
    u0_original: jnp.ndarray,
    u0_pool: jnp.ndarray,
    model_state,
    predict_batch,
    t_end_vec: jnp.ndarray,
    rollout_steps: int,
    additions_done: int,
    num_initial_ics: int,
    num_vars: int,
) -> jnp.ndarray:
    """
    Expand the IC pool by rolling out u0_original `rollout_steps` windows
    forward using the current network, then writing the result into the next
    free slot of u0_pool.
 
    Returns the updated u0_pool.
    """
    single_params = jax.device_get(
        tree_map(lambda x: x[0], model_state)
    ).params
 
    u_rolled = u0_original
    for _ in range(rollout_steps):
        u_rolled = predict_batch(single_params, u_rolled, t_end_vec)
        u_rolled = u_rolled.reshape(num_initial_ics, num_vars)
 
    slot_start = num_initial_ics * (additions_done + 1)
    slot_end   = slot_start + num_initial_ics
    u0_pool    = u0_pool.at[slot_start:slot_end].set(u_rolled)
    return u0_pool

 
